public class Square{
    String Type;
    int Id;
    int Pos;
    int PriceTag;
    int Rent;

    public void setType(String type) {
        Type = type;
    }

    public String getType() {
        return Type;
    }

    public void setId(int id) {
        Id = id;
    }

    public int getId() {
        return Id;
    }

    public void setPos(int pos) {
        Pos = pos;
    }

    public int getPos() {
        return Pos;
    }

    public void setPriceTag(int priceTag) {
        PriceTag = priceTag;
    }

    public int getPriceTag() {
        return PriceTag;
    }

    public void setRent(int rent) {
        Rent = rent;
    }

    public int getRent() {
        return Rent;
    }
}